# -*- coding: utf-8 -*-
import re

def replaceWords(dictionary,string):
	keys = [x.decode('utf-8') for x in dictionary.keys()]
	pattern = re.compile(r'\b(' + '|'.join(keys) + r')\b')
	result = pattern.sub(lambda x: dictionary[x.group()], string)
	return result

def replacePhrase(dictionary,string):
	keys = [x.decode('utf-8') for x in dictionary.keys()]
	dictionary = dict((re.escape(k), v) for k, v in dictionary.iteritems())
	pattern = re.compile("|".join(keys))
	text = pattern.sub(lambda m: dictionary[re.escape(m.group(0))], string)
	return text

